# Exemplo 8 - Cálculo de raiz quadrada
import math

num = float(input("Digite um número: "))
valor = math.sqrt(num)
print("\nO valor da raiz quadrada é: ", valor)
print("\nO valor da raiz quadrada é: %.2f" % (valor))

#\n = pula linha
#\n = pula 2 linhas
#%.2f = 2 casas decimais
#square root = raiz quadrada
